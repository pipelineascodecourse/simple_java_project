package my.simplejavaproject;

public class Calculator {
         //add function
	public int add(int a, int b)
	{
		return a+b;
	}
        // Subtract function
	public int minus(int a, int b)
	{
		return a-b;
	}
	// Division function
	public int divide(int a, int b)
	{
		return a/b;
	}
	// Multiplication function
	public int multiply(int a, int b)
	{
		return a*b;
	}
	
}
